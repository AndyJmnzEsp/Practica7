    private void jtPasilloKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtPasilloKeyTyped
        // TODO add your handling code here:
        int tam = 1;
        char caracter = evt.getKeyChar();
        jlmensaje.setVisible(false);
        if (caracter == KeyEvent.VK_ENTER){
            jbAgregar.requestFocus();
        }else{
            if (!Character.isDigit(caracter)){
                getToolkit().beep();
                evt.consume();
                jlmensaje.setVisible(true);
                jlmensaje.setText("Solo capturar numeros");
            }
            if (jtPasillo.getText().length() >= tam && caracter != KeyEvent.VK_ENTER ){
                getToolkit().beep();
                evt.consume();
                jlmensaje.setVisible(true);
                jlmensaje.setText("Max 2 digitos");
            }

        }
    }//GEN-LAST:event_jtPasilloKeyTyped

    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
         String T = jtTitulo.getText();
         String A = jtAutor.getText();
         String S = jtSeccion.getText();
        int P = Integer.parseInt(jtPasillo.getText());
        ListaLPilaLibro.push(T,A,S,P);
        jtTitulo.setText("");
        jtAutor.setText("");
        jtSeccion.setText("");
        jtPasillo.setText("");
        jtTitulo.requestFocus();
        jlmensaje.setVisible(true);
        jlmensaje.setText("Se agrego a la pila");

    }//GEN-LAST:event_jbAgregarActionPerformed

