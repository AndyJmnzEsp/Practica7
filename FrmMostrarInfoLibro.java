    public FrmMostrarInfoLibro(FrmMenu menu,PilaLibro datos) {
        initComponents();
        principal = menu;
        
        Libro aux = datos.pilaLibro;
        if (datos.pilaLibro==null){
            jtaArea.setText("Pila vacia.");
        }else{
            jtaArea.setText("Informacion dentro de la pila: \n");
            while (aux != null) {
                jtaArea.append("\n" + "Titulo: " + aux.getTitulo()+ "\n"+ "Autor: " + aux.getAutor()+ "\n"+"Seccion: " + aux.getSeccion()+ "\n"+"Pasillo: " + aux.getPasillo()+ "\n");
                aux = aux.getSiguienteLibro();
            }
        }
    }

