    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // TODO add your handling code here:
        int R = Integer.parseInt(jtRegistro.getText());
        String N = jtNombre.getText();
        int G = Integer.parseInt(jtGrado.getText());
        float P = Float.parseFloat(jtPromedio.getText());
        ListaAPila.push(R, N, G, P);
        jtRegistro.setText("");
        jtNombre.setText("");
        jtGrado.setText("");
        jtPromedio.setText("");
        jtRegistro.requestFocus();
        jlmensaje.setVisible(true);
        jlmensaje.setText("Se agrego a la pila");
                
        
    }//GEN-LAST:event_jbAgregarActionPerformed

