    private void jtRegistroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtRegistroKeyTyped
        // TODO add your handling code here:
        int tam = 8;
        char caracter = evt.getKeyChar();
        jlmensaje.setVisible(false);
        if (caracter == KeyEvent.VK_ENTER){
            jtNombre.requestFocus();
        }else{
            if (!Character.isDigit(caracter)){
                getToolkit().beep();
                evt.consume();
                jlmensaje.setVisible(true);
                jlmensaje.setText("Solo capturar numeros");
            }
            if (jtRegistro.getText().length() >= tam && caracter != KeyEvent.VK_ENTER){
                getToolkit().beep();
                evt.consume();
                jlmensaje.setVisible(true);
                jlmensaje.setText("Max 8 digitos");
            }
        }
    }//GEN-LAST:event_jtRegistroKeyTyped

